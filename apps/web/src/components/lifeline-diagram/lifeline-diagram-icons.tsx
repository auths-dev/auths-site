const ICON_MAP: Record<string, ComponentType<IconProps>> = {
  user: UserIcon,
  git: GitIcon,
  key: KeyIcon,
  terminal: TerminalIcon,
  shield: ShieldIcon,
  globe: GlobeIcon,
  database: DatabaseIcon,
  lock: LockIcon,
};

/**
 * Resolves a string icon ID to its React component.
 *
 * @param id - One of: user, git, key, terminal, shield, globe, database, lock
 * @returns The matching icon component, or UserIcon as fallback
 *
 * @example
 * const Icon = resolveIcon('git');
 * <Icon size={16} className="text-amber-400" />
 */
export function resolveIcon(id: string): ComponentType<IconProps> {
  return ICON_MAP[id] ?? UserIcon;
}
